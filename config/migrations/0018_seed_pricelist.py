def seed(apps, schema_editor):
    Category = apps.get_model('core', 'Category')
    Product = apps.get_model('core', 'Product')

    def upsert(name, category, price):
        price = Decimal(price)
        obj, created = Product.objects.get_or_create(
            name=name,
            defaults={
                'category': category,
                'unit': 'шт',
                'stock_total': 0,
                'daily_price': price,
                'deposit_per_unit': 0,
                'is_active': True,
            },
        )
        if created:
            return
        fields = []
        if obj.daily_price != price:
            obj.daily_price = price
            fields.append('daily_price')
        if not obj.is_active:
            obj.is_active = True
            fields.append('is_active')
        if fields:
            obj.save(update_fields=fields)

    for cat_name, prefix, rows in GROUPS:
        cat, _ = Category.objects.get_or_create(name=cat_name)
        for size, price in rows:
            upsert(f'{prefix} {size}', cat, price)

    for name, price in SINGLES:
        Product.objects.filter(name=name).update(daily_price=Decimal(price))

    Product.objects.filter(name__in=DEACTIVATE).update(is_active=False)


